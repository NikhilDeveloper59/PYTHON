# A Dictionary is a collection in Python that stores data in Key : Value pairs.

# ✅ Properties of Dictionary

# ✅ Stores data as key:value
# ✅ Keys are unique (no duplicates)
# ✅ Values can be duplicate
# ✅ Mutable (changeable)
# ✅ Keys must be immutable (string, int, tuple)
# ❌ list/set cannot be keys

# Example:
# student = {
#     "name": "Nikhil",
#     "roll": 101,
#     "marks": 92
# }
